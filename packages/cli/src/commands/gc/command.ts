import type { CommandMetadata } from "@shell-shock/core";
import { removeDirectory } from "@stryke/fs";
import { createEngine } from "powerlines";

export const metadata = {
  title: "Garbage Collection",
  description:
    "Perform garbage collection to clean up unnecessary system files based on the specified options. This can help free up disk space and improve system performance. \n\nNote: The actual types of environment paths available for cleanup may vary based on the project's configuration and the underlying system's operating system.",
  icon: "🗶",
  tags: ["Utility"],
  alias: ["prune", "garbage-collection"]
} satisfies CommandMetadata;

export interface GarbageCollectionOptions {
  /**
   * The type of environment paths to clean up. Can be "data", "cache", "logs", "temp", or "all" to clean all types.
   *
   * @remarks
   * This option allows you to specify which types of environment paths should be targeted for garbage collection. Depending on the project's configuration and the underlying system's operating system, the actual types of environment paths available for cleanup may vary. Common types include:
   * - "data": User-specific data files.
   * - "cache": Temporary files used for caching purposes.
   * - "logs": Log files generated by the application.
   * - "temp": Temporary files that may not fit into the other categories.
   * - "all": Clean all types of environment paths.
   */
  type?: "data" | "cache" | "logs" | "temp" | "all";
}

/**
 * Perform garbage collection to clean up unnecessary system files based on the specified options. This can help free up disk space and improve system performance.
 *
 * @param _options - The options for garbage collection.
 * @param type - The type of environment paths to clean up. Can be "data", "cache", "logs", "temp", or "all" to clean all types.
 * @returns A promise that resolves when the garbage collection process is complete.
 */
async function handler(
  _options: GarbageCollectionOptions = {},
  type: "data" | "cache" | "logs" | "temp" | "all" = "all"
) {
  const api = await createEngine({
    cwd: process.cwd(),
    root: process.cwd()
  });

  const envPaths = [] as string[];
  if (type === "all" || type === "data") {
    envPaths.push(api.context.envPaths.data);
  }
  if (type === "all" || type === "cache") {
    envPaths.push(api.context.envPaths.cache);
  }
  if (type === "all" || type === "logs") {
    envPaths.push(api.context.envPaths.log);
  }
  if (type === "all" || type === "temp") {
    envPaths.push(api.context.envPaths.temp);
  }

  await Promise.all(
    envPaths.map(async path => {
      await removeDirectory(path);
    })
  );
}

export default handler;
